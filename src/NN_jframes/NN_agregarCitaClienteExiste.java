    private void B_noActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_B_noActionPerformed
        // TODO add your handling code here:
        if(fromEdit){
            editarCitaMenu.reset();    
            editarCitaMenu.enable(true);
        }
        else{
            agregarCitaMenu.reset();
            agregarCitaMenu.enable(true);
        }
        fromEdit = false;
        this.dispose();
    }//GEN-LAST:event_B_noActionPerformed

    private void B_yesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_B_yesActionPerformed
        // TODO add your handling code here:
        if(fromEdit){
        editarCitaMenu.finishAppointment();
        editarCitaMenu.setEnabled(true);
        }
        else{
        agregarCitaMenu.finishAppointment();
        agregarCitaMenu.setEnabled(true);            
        }
        this.dispose();
    }//GEN-LAST:event_B_yesActionPerformed

